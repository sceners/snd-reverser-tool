		xor eax, K[ecx*8].u64.Lo
		xor edx, K[ecx*8].u64.Hi
		mov [edi+ecx*8].u64.Lo, eax
		mov [edi+ecx*8].u64.Hi, edx
		inc ecx
	.until ecx==8
	add esp, Whirlpoollocals
	popad
	ret
WhirlpoolTransform endp

WhirlpoolBURN macro
	xor eax, eax
	mov WhirlpoolIndex, eax
	mov edi, Offset WhirlpoolHashBuf
	mov ecx, (sizeof WhirlpoolHashBuf)/4
	rep stosd
endm

align 4
WhirlpoolInit proc uses edi esi
	xor eax, eax
	mov WhirlpoolLen, eax
	mov edi, Offset WhirlpoolDigest
	mov ecx, (sizeof WhirlpoolDigest)/4
	rep stosd	
	WhirlpoolBURN
	mov eax, Offset WhirlpoolDigest 
