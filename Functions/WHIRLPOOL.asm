		mov	edi, [eax+ecx].u64.Hi
		bswap esi
		bswap edi
		mov	[eax+ecx].u64.Hi, esi
		mov	[eax+ecx].u64.Lo, edi
		add ecx, 8
	.until ecx==8*8	
	ret
WhirlpoolFinal endp

