WhirlpoolInit endp

align 4
WhirlpoolUpdate proc uses esi edi ebx lpBuffer:dword, dwBufLen:dword
	mov ebx, dwBufLen
	add WhirlpoolLen, ebx
	.while ebx
		mov eax, WhirlpoolIndex
		mov edx, 64
		sub edx, eax
		.if edx <= ebx
			lea edi, [WhirlpoolHashBuf+eax]	
			mov esi, lpBuffer
			mov ecx, edx
			rep movsb
			sub ebx, edx
			add lpBuffer, edx
			call WhirlpoolTransform
			WhirlpoolBURN
		.else
			lea edi, [WhirlpoolHashBuf+eax]	
			mov esi, lpBuffer
			mov ecx, ebx
			rep movsb
			mov eax, WhirlpoolIndex
			add eax, ebx
			mov WhirlpoolIndex, eax
			.break
		.endif
	.endw
	ret
WhirlpoolUpdate endp

align 4
WhirlpoolFinal proc uses esi edi
	mov ecx, WhirlpoolIndex
	mov byte ptr [WhirlpoolHashBuf+ecx], 80h
	.if ecx >= 32
		call WhirlpoolTransform
		WhirlpoolBURN
	.endif
	mov eax, WhirlpoolLen
	xor edx, edx
	shld edx, eax, 3
	shl eax, 3
	bswap edx
	bswap eax
	mov dword ptr [WhirlpoolHashBuf+56], edx
	mov dword ptr [WhirlpoolHashBuf+60], eax
